#!/usr/bin/env python3
from __future__ import annotations

import argparse
import json
from pathlib import Path
from typing import Any


LEVELS = {
    "exact-captured",
    "parse-only-safe",
    "approximated",
    "unimplemented-loud",
    "forbidden-io",
}


def c_string(value: str) -> str:
    return '"' + value.replace("\\", "\\\\").replace('"', '\\"') + '"'


def load_contracts(path: Path) -> list[dict[str, str]]:
    payload: Any = json.loads(path.read_text(encoding="utf-8"))
    stubs = payload.get("stubs")
    if not isinstance(stubs, list):
        raise ValueError("stubs must be an array")
    seen: set[str] = set()
    out: list[dict[str, str]] = []
    for item in stubs:
        if not isinstance(item, dict):
            raise ValueError("stub item must be object")
        name = item.get("name")
        level = item.get("level")
        summary = item.get("summary")
        if not isinstance(name, str) or not name:
            raise ValueError("stub name must be non-empty string")
        if name in seen:
            raise ValueError(f"duplicate stub contract: {name}")
        if level not in LEVELS:
            raise ValueError(f"invalid level for {name}: {level}")
        if not isinstance(summary, str) or not summary:
            raise ValueError(f"summary must be non-empty string for {name}")
        seen.add(name)
        out.append({"name": name, "level": level, "summary": summary})
    return sorted(out, key=lambda item: item["name"])


def write_header(path: Path, contracts: list[dict[str, str]]) -> None:
    lines = [
        "/* Generated by scripts/build/build_stub_contracts.py. */",
        "#ifndef CURLPARSE_STUB_CONTRACTS_GENERATED_H",
        "#define CURLPARSE_STUB_CONTRACTS_GENERATED_H",
        "",
        "struct CurlparseStubContractEntry {",
        "  const char *name;",
        "  const char *level;",
        "  const char *summary;",
        "};",
        "",
        "static const struct CurlparseStubContractEntry curlparse_stub_contracts[] = {",
    ]
    for item in contracts:
        lines.append(
            "  { "
            f"{c_string(item['name'])}, "
            f"{c_string(item['level'])}, "
            f"{c_string(item['summary'])} "
            "},"
        )
    lines.extend([
        "};",
        "",
        "#define CURLPARSE_STUB_CONTRACT_COUNT \\",
        "  (sizeof(curlparse_stub_contracts) / sizeof(curlparse_stub_contracts[0]))",
        "",
        "#endif",
    ])
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text("\n".join(lines) + "\n", encoding="utf-8")


def write_docs(path: Path, contracts: list[dict[str, str]]) -> None:
    lines = [
        "# Stub Contracts",
        "",
        "Generated from `core/c/src/runtime/stub-contracts.json`.",
        "",
        "| Stub | Level | Summary |",
        "| --- | --- | --- |",
    ]
    for item in contracts:
        lines.append(f"| `{item['name']}` | `{item['level']}` | {item['summary']} |")
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text("\n".join(lines) + "\n", encoding="utf-8")


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--contracts", required=True)
    parser.add_argument("--header", required=True)
    parser.add_argument("--docs")
    args = parser.parse_args()

    contracts = load_contracts(Path(args.contracts))
    write_header(Path(args.header), contracts)
    if args.docs:
        write_docs(Path(args.docs), contracts)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
